const int N = 1e4 + 1;

int n;
short int a[N], b[N];
short int dp[N][N];

int main() {
    in(n);
    for (int i = 0; i < n; i++) {
        in(a[i]);
    }
    int len, l, h;
    for (len = 1; len < n; len++) {
        for (l = 0, h = len; h < n; ++l, ++h) {
            dp[l][h] = (a[l] == a[h] ? dp[l + 1][h - 1] : min(dp[l][h - 1], dp[l + 1][h]) + 1);
        }
    }
    out(dp[0][n - 1], '\n');
    return 0;
}